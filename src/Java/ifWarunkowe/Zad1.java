package Java.ifWarunkowe;

public class Zad1 {

    public Zad1() {
    }

    public static void main(String[] args) {
        int number = 99;
        if (number > 100) {
            System.out.println("Liczba jest większa niż 100");
        } else if (number == 100) {
            System.out.println("Liczba jest równa 100");
        } else {
            System.out.println("Liczba jest mniejsza niż 100");
        }

    }
}
